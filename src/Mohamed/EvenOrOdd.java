package Mohamed;

import java.util.Scanner;

public class EvenOrOdd {

    public static void main(String[] args) {

        checkNumber(7);
    }

    public static void checkNumber (int num){



        if( num % 2 == 0){
            System.out.println("Your number is Even!");
        }else {
            System.out.println("Your number is Odd!");

        }

    }



}
